    private void addRowTojTable(){
        DefaultTableModel model = (DefaultTableModel) listContacts.getModel();
        ContactUtilization x = new ContactUtilization(); 
        List<Person> list = x.todosContactos();
        Object rawdata[] = new Object[6];
        for (int i = 0; i<list.size(); i++){
            rawdata[0] = list.get(i).name;
            rawdata[1] = list.get(i).lastName;
            rawdata[2] = list.get(i).dni;
            rawdata[3] = list.get(i).city;
            rawdata[4] = list.get(i).address;
            rawdata[5] = list.get(i).phone;
            model.addRow(rawdata);
        }
    }
