    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ContactUtilization x = new ContactUtilization(); 
        List<Person> list = null;
        switch(searchButton.getSelectedItem().toString()){
            case "Apellido":
                list = x.buscarCliente(searchFiedl.getText());
                break;
            case "Ciudad":
                list = x.buscarClientes(searchFiedl.getText());
                break;
            case "Telefono":
                list = x.buscarTeléfono(searchFiedl.getText());
                break;
        }
        System.out.println("The Value is: " + searchButton.getSelectedItem().toString());
        System.out.println("The Value is: " + searchFiedl.getText());
        DefaultTableModel model = (DefaultTableModel) listContacts.getModel();
        model.setRowCount(0);
        Object rawdata[] = new Object[6];
        for (int i = 0; i<list.size(); i++){
            rawdata[0] = list.get(i).name;
            rawdata[1] = list.get(i).lastName;
            rawdata[2] = list.get(i).dni;
            rawdata[3] = list.get(i).city;
            rawdata[4] = list.get(i).address;
            rawdata[5] = list.get(i).phone;
            model.addRow(rawdata);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

